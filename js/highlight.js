
function prijmySazbaHighlight() {

    if ($("#prijmySazba").val() != "") {
		$("#prijmySazba").css({"border-color": "green",
		"border-width":"1px",
		"border-style":"solid"});
	}

	else {
		$("#prijmySazba").css({"border-color": "red",
		"border-width":"1px",
		"border-style":"solid"});
	}

}

